public class OddEven {


    private Integer[] odds;
     private Integer[] evens;

    public OddEven(Integer[] odds, Integer[] evens) {
        this.odds = odds;
        this.evens = evens;
    }

    public Integer[] getOdds(){
        return odds;
    }

    public Integer[] getEvens(){
        return evens;
    }

}
