public class MaxMin {

    private int max;
    private int min;

    public MaxMin(int max, int min) {
        this.max = max;
        this.min = min;
    }

    public MaxMin(){}

    public void setMax(int max) {
        this.max = max;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public Object getMax() {
        return max;
    }

    public Object getMin() {
        return min;
    }
}
